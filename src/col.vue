<template>
    <div class="col" :class="colClass"
       :style="colStyle"
    >
        <div style="border:1px solid green;height:100px;">
            <slot></slot>
        </div>

    </div>
</template>

<script>
    export default {
        // name:'GuluCol',
        props: {
            span: {
                type: [Number, String]
            },
            offset: {
                type: [Number,String]
            },
            // gutter:{
            //     type: [Number,String]
            // }

        },
        data(){
            return {
                gutter:0,

            }
        },
        computed:{
            colStyle(){
                return{
                    paddingLeft:this.gutter/2+'px',
                    paddingRight:this.gutter/2+'px'
                }
            },
            colClass(){
                let {span,offset}=this
                return [
                    span &&`col-${span}`,
                    offset && `offset-${offset}`
                ]
            }

        },

        created(){
            console.log(this.$children);
        },
        mounted(){
            console.log(this.$children);
        }
    }

</script>

